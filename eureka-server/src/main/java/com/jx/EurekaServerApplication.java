/**
 * @author Edison Wang
 * @version 1.0
 * @description TODO
 * @date 2023/5/11 14:07
 */

@EnableEurekaServer
@SpringBootApplication
public class EurekaServerApplication {
    public static void main(String[] args) {
        SpringApplication.run(EurekaServerApplication.class,args);
    }
}
